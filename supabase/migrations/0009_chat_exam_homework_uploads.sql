alter table public.homework add column if not exists file_url text;
alter table public.homework add column if not exists file_name text;
alter table public.homework add column if not exists file_type text;

alter table public.homework_status add column if not exists answer_file_url text;
alter table public.homework_status add column if not exists answer_file_name text;
alter table public.homework_status add column if not exists feedback text;
alter table public.homework_status add column if not exists submitted_at timestamptz;

-- Same shape as the exam_scores policies above: a student can insert/
-- update their own row while it isn't graded yet, can only move it to
-- 'Assigned' or 'Submitted' (never 'Graded'), and can never write a score
-- or feedback themselves.
drop policy if exists homework_status_student_submit on public.homework_status;
create policy homework_status_student_submit on public.homework_status for insert
  with check (
    exists (select 1 from public.students s where s.id = homework_status.student_id and s.profile_id = auth.uid())
    and status in ('Assigned', 'Submitted')
    and score is null
    and feedback is null
  );

drop policy if exists homework_status_student_update_own on public.homework_status;
create policy homework_status_student_update_own on public.homework_status for update
  using (
    exists (select 1 from public.students s where s.id = homework_status.student_id and s.profile_id = auth.uid())
    and status <> 'Graded'
  )
  with check (
    exists (select 1 from public.students s where s.id = homework_status.student_id and s.profile_id = auth.uid())
    and status in ('Assigned', 'Submitted')
    and score is null
    and feedback is null
  );

