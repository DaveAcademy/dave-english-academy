-- A student may create/update their own exam_scores row to attach an
-- answer file, but only while it's still ungraded (score is null) - and
-- can never set a score themselves (with check also requires score is
-- null on the row they're writing). Once a teacher/admin grades it,
-- these policies stop matching and only the existing admin/teacher "for
-- all" policies apply.
drop policy if exists exam_scores_student_submit on public.exam_scores;
create policy exam_scores_student_submit on public.exam_scores for insert
  with check (
    exists (select 1 from public.students s where s.id = exam_scores.student_id and s.profile_id = auth.uid())
    and score is null
  );

drop policy if exists exam_scores_student_update_own on public.exam_scores;
create policy exam_scores_student_update_own on public.exam_scores for update
  using (
    exists (select 1 from public.students s where s.id = exam_scores.student_id and s.profile_id = auth.uid())
    and score is null
  )
  with check (
    exists (select 1 from public.students s where s.id = exam_scores.student_id and s.profile_id = auth.uid())
    and score is null
  );

